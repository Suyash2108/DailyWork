package com.cg.typecasting;

public class ConvertingIntToString {

	public static void main(String[] args) {
		double num = 11.99;
	    System.out.println("The double value: " + num);

	    // convert into int type
	    int data = (int)num;
	    System.out.println("The integer value: " + data);
	  }
	}

