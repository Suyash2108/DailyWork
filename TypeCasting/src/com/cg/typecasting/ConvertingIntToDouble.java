package com.cg.typecasting;

public class ConvertingIntToDouble {

	public static void main(String[] args) {
		
		int num = 20;
	    System.out.println("The integer value: " + num);

	    // convert into double type
	    double data = num;
	    System.out.println("The double value: " + data);
	  }
	}